# Numbers

print(2 + 4)
print(2 - 4)
print(2 * 4)
print(2 / 4)

print(type(2 + 4))    # <class int>
print(type(2 / 4))    # <class float>

print(2 ** 3)   # 8
print(5 // 4)   # 1
print(6 % 4)    # 2

print(round(3.1))   # 3         - rounds the number
print(abs(-5))      # 5         - returns absolute value (no negative number)
print(bin(5))       # you get the binary representation after the b     0b101
print(int('0b101', 2))      # convert from binary to base 2 number      5

